def sortuj_litery(ciag):
    return ''.join(sorted(ciag))

print("Oryginalny ciąg: PHP")
print("Posortuj litery ciągu:", sortuj_litery("PHP"))

print("Oryginalny ciąg: javascript")
print("Posortuj litery ciągu:", sortuj_litery("javascript"))

print("Oryginalny ciąg: python")
print("Posortuj litery ciągu:", sortuj_litery("python"))