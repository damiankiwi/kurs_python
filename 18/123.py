def usun_pierwszy_i_ostatni(ciag):
    return ciag[1:-1]

print("Oryginalny ciąg: PHP")
print("Usunięcie pierwszego i ostatniego elementu:", usun_pierwszy_i_ostatni("PHP"))

print("Oryginalny ciąg: Python")
print("Usunięcie pierwszego i ostatniego elementu:", usun_pierwszy_i_ostatni("Python"))

print("Oryginalny ciąg: JavaScript")
print("Usunięcie pierwszego i ostatniego elementu:", usun_pierwszy_i_ostatni("JavaScript"))